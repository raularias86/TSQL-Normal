SELECT COD_ASIGNADO, DECODE(COD_AREA_FINANCIERA, 1120,'CORPORATIVO',1113,'EMPRESARIAL','OTRO') UNIDAD,  
       FCH_PROCESO, EXTRACT(MONTH FROM FCH_PROCESO) MES, 
       NO_UNICO, CLIENTE, NVL(GRUPO_ECONOMICO,'SIN GRUPO ECONÓMICO') GRUPO_ECONOMICO, SECTOR_ECONOMICO, 
       DECODE(SEGMENT1,'PORTAFOLIO DEPOSITOS','PASIVO','ACTIVO') TIPO_PRODUCTO, SEGMENT2, PRODUCTO, COUNT(DISTINCT NO_CUENTA) NO_PRODUCTOS,
       SUM(MONTO) MONTOS, SUM(SALDO) SALDO_CONTABLE  
FROM   DASHBA.VWDASH_PORTAFOLIO_HIST O100416 
WHERE FCH_PROCESO IN  --TO_DATE('20160531000000','YYYYMMDDHH24MISS')  --= (SELECT MAX(FCH_PROCESO) FROM DASHBA.VWDASH_PORTAFOLIO_HIST) 
      (TO_DATE('20160131','YYYYMMDD'),
       TO_DATE('20160229','YYYYMMDD'),
       TO_DATE('20160331','YYYYMMDD'),
       TO_DATE('20160430','YYYYMMDD'),
       TO_DATE('20160531','YYYYMMDD'),
       TO_DATE('20160620','YYYYMMDD'))
--AND COD_AREA_FINANCIERA = 1113
AND COD_ASIGNADO IN (601,604,605,606,607,608,609,610,611,612,614,615,616,617,619,698,1,3,4,5,6,7,9,10,11,12,19,2)
AND SEGMENT1 IN ('1. PORTAFOLIO PRESTAMOS','PORTAFOLIO DEPOSITOS')
AND STATUS != 'S'
--and grupo_economico is not null
group by COD_ASIGNADO, DECODE(COD_AREA_FINANCIERA, 1120,'CORPORATIVO',1113,'EMPRESARIAL','OTRO'), 
         FCH_PROCESO, EXTRACT(MONTH FROM FCH_PROCESO), 
         NO_UNICO, CLIENTE, NVL(GRUPO_ECONOMICO,'SIN GRUPO ECONÓMICO'), SECTOR_ECONOMICO, 
         DECODE(SEGMENT1,'PORTAFOLIO DEPOSITOS','PASIVO','ACTIVO'), SEGMENT2, PRODUCTO 
order by NO_UNICO, DECODE(SEGMENT1,'PORTAFOLIO DEPOSITOS','PASIVO','ACTIVO') 
