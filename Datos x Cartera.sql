SELECT COD_ASIGNADO, FCH_PROCESO, EXTRACT(MONTH FROM FCH_PROCESO) MES, 
       DECODE(COD_AREA_FINANCIERA, 1120,'CORPORATIVO',1113,'EMPRESARIAL','OTRO') UNIDAD, 
       DECODE(SEGMENT1,'PORTAFOLIO DEPOSITOS','PASIVOS','ACTIVOS') TIPO_PRODUCTO, NVL(SECTOR_ECONOMICO,'SIN SECTOR ECONOMICO') SECTOR_ECONOMICO,
       SUM(SALDO) SALDO, COUNT(NO_CUENTA) CUENTAS, SUM(MONTO) MONTOS-- segment1, COUNT(distinct no) 
FROM   DASHBA.VWDASH_PORTAFOLIO_HIST O100416
WHERE FCH_PROCESO IN  --TO_DATE('20160531000000','YYYYMMDDHH24MISS')  --= (SELECT MAX(FCH_PROCESO) FROM DASHBA.VWDASH_PORTAFOLIO_HIST) 
      (TO_DATE('2016013100000','YYYYMMDDHH24MISS'),
       TO_DATE('2016022900000','YYYYMMDDHH24MISS'),
       TO_DATE('2016033100000','YYYYMMDDHH24MISS'),
       TO_DATE('2016043000000','YYYYMMDDHH24MISS'),
       TO_DATE('2016053100000','YYYYMMDDHH24MISS'),
       TO_DATE('20160620','YYYYMMDD'))
--AND COD_AREA_FINANCIERA = 1120
AND COD_ASIGNADO IN (601,604,605,606,607,608,609,610,611,612,614,615,616,617,619,698,1,3,4,5,6,7,9,10,11,12,19,2)
AND SEGMENT1 IN ('1. PORTAFOLIO PRESTAMOS', 'PORTAFOLIO DEPOSITOS')
AND STATUS != 'S'
group by COD_ASIGNADO, FCH_PROCESO, EXTRACT(MONTH FROM FCH_PROCESO), 
       DECODE(COD_AREA_FINANCIERA, 1120,'CORPORATIVO',1113,'EMPRESARIAL','OTRO'), 
       DECODE(SEGMENT1,'PORTAFOLIO DEPOSITOS','PASIVOS','ACTIVOS'), NVL(SECTOR_ECONOMICO,'SIN SECTOR ECONOMICO')
ORDER BY COD_ASIGNADO, FCH_PROCESO, DECODE(COD_AREA_FINANCIERA, 1120,'CORPORATIVO',1113,'EMPRESARIAL','OTRO'), NVL(SECTOR_ECONOMICO,'SIN SECTOR ECONOMICO')