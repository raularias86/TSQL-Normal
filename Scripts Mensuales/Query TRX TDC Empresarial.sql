SELECT  TO_CHAR(O838514.CARD_NUMBER) CARD_NUMBER, 
        O838514.AS_OF_DATE, O838514.TRANSACTION_CODE, O838514.TRANSACTION_DATE, 
        O838514.AUTHORIZATION_CODE, O838514.MCATEGORY_CODE, O838514.MERCHANT_CODE, O838514.MERCHANT_COUNTRY,
        TO_CHAR(NVL(O838514.MERCHANT_NAME,'SIN NOMBRE')) MERCHANT_NAME, TO_CHAR(NVL(O838514.MERCHANT_TYPE_DESC,'S/N')) MERCHANT_TYPE_DESC, 
        O838514.ORIGEN_CODE, 
        ( SUBSTR(O838514.ACCOUNT_NUMBER,1,6) ) NO_CUENTA, 
        SUM(O838514.TOTAL_AMOUNT) MONTO_TRX
FROM DWBA.CC_TRANS_DIA O838514
WHERE ( O838514.TRANSACTION_CODE IN ('02','06','IN') ) AND 
      ( ( SUBSTR(O838514.ACCOUNT_NUMBER,1,6) ) IN ('456795','403774') ) AND 
      ( O838514.AS_OF_DATE IN (( TO_DATE('20160531000000','YYYYMMDDHH24MISS') )) )
GROUP BY O838514.CARD_NUMBER, O838514.AS_OF_DATE, O838514.TRANSACTION_CODE, 
         O838514.TRANSACTION_DATE, O838514.AUTHORIZATION_CODE, O838514.MCATEGORY_CODE, 
         O838514.MERCHANT_CODE, O838514.MERCHANT_COUNTRY, O838514.MERCHANT_NAME, 
         O838514.MERCHANT_TYPE_DESC, O838514.ORIGEN_CODE, ( SUBSTR(O838514.ACCOUNT_NUMBER,1,6) )
         
         
