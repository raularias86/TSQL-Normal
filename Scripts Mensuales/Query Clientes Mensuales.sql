SELECT 
O985185.CIFFECHAPROCESO AS FECHA_PROCESO, 
O985185.CIFCODCLIENTE   AS NO_UNICO,
TO_CHAR(NVL(O985185.CIFNOMBRECLIE,'S/N'))   AS NOMBRE_CLIENTE,
O985185.CIFCODEJECUTI   AS COD_ASIGNADO, 
O985185.CIFCODAREAFIN   AS COD_AREA_FIN, 
O100794.FINDESAREAFIN   AS AREA_FIN, 
O985185.CIFSEGMENTO     AS SEGMENTO_INGRESOS    --SELECT DISTINCT  O985185.CIFFECHAPROCESO
FROM BACATALOG.CATAREAFINANC O100794,            
     BACATALOG.CATEJECUTIVOS O592621, 
     DWBA.CIFGENERALES @BARIESGOS  O985185
WHERE ( ( O100794.FINCODAREAFIN = O592621.FINCODAREAFIN ) 
AND   ( O592621.FINCODEJECUTIV(+) = O985185.CIFCODEJECUTI ) ) 
AND   ( O985185.CIFFECHAPROCESO = TO_DATE('20160131000000','YYYYMMDDHH24MISS') ) --CAMBIAR FECHA
