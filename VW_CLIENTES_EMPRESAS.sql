CREATE VIEW VW_CLIENTES_EMPRESAS 

AS 

SELECT FECHA_PROCESO, NO_UNICO, NOMBRE_CLIENTE, COD_ASIGNADO, COD_AREA_FIN, AREA_FIN, SEGMENTO_INGRESOS,
	   NIT, MASTER, NOMBRE_GRUPO, FCH_INGRESO, TIPO_GRUPO, CODDOMICILIO 
FROM CLIENTES
WHERE COD_AREA_FIN IN (1103,1106,1113,1112,1111,1120,1123,1101)
AND FECHA_PROCESO >= '2017-01-01'
