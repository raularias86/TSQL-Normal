SELECT  O268691.FCH_PROCESO, O268691.NO_UNICO, COD_ASIGNADO, COD_AREA_FINANCIERA,
        O268691.SEGMENT1, SEGMENT2 , COUNT(DISTINCT NO_CUENTA) REFERENCIAS
FROM DASHBA.DASH_PORTAFOLIO_HIST@BASIG O268691
WHERE --(O268691.FCH_PROCESO = TO_DATE('20180630000000','YYYYMMDDHH24MISS') 
      ( O268691.FCH_PROCESO IN (SELECT MAX(FCH_PROCESO) FROM DASHBA.DASH_PORTAFOLIO_HIST@BASIG WHERE EXTRACT(YEAR FROM FCH_PROCESO) = 2018
							GROUP BY EXTRACT(MONTH FROM FCH_PROCESO)))
      EXTRACT(MONTH FROM FCH_PROCESO) >= 9 
      AND ( O268691.STATUS != 'S')
      AND ( O268691.COD_AREA_FINANCIERA IN (1103,1106,1113,1112,1111,1120,1123,1101))
      AND ( O268691.SEGMENT1 NOT IN ('1. PORTAFOLIO PRESTAMOS','PORTAFOLIO DEPOSITOS') )     
GROUP BY O268691.FCH_PROCESO, O268691.NO_UNICO, COD_ASIGNADO, COD_AREA_FINANCIERA,
        O268691.SEGMENT1, SEGMENT2 
ORDER BY O268691.FCH_PROCESO, O268691.NO_UNICO, COD_ASIGNADO, COD_AREA_FINANCIERA,
        O268691.SEGMENT1, SEGMENT2        